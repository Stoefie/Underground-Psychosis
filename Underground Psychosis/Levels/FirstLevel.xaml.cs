        public FirstLevel()
        {
            InitializeComponent();

            Loaded += (s, e) =>
            {
                _game = new GameLoop(GameCanvas);
                _player = new Player { Position = new System.Windows.Point(100, 850) };
                _game.AddEntity(_player);
                LoadLevel(levelMap);
                _game.Start();
            };
        }
        private void LoadLevel(int[,] map)
        {
            int rows = map.GetLength(0);
            int cols = map.GetLength(1);

            double tileWidth = GameCanvas.ActualWidth / cols;
            double tileHeight = GameCanvas.ActualHeight / rows;

            for (int y = 0; y < rows; y++)
                { 
                for (int x = 0; x < cols; x++) 
                    {
                    if (map[y, x] == 1)
                        {
                            var tile = new Tile(x, y, tileWidth, tileHeight, true);
                            _game.AddEntity(tile);
                        }
                    }
                }
        }
